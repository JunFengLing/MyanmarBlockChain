function getCenter(coords) {
  let east, south, west, north;
  let latList = [];
  let lngList = [];
  coords.forEach(item => {
    latList.push(Number(item.lat));
    lngList.push(Number(item.lng));
  });
  latList = latList.sort();
  lngList = lngList.sort();
  east =  lngList[lngList.length - 1];
  south = latList[0];
  west = lngList[0];
  north = latList[latList.length - 1];

  return {
    lat: (north + south) / 2,
    lng: (east + west) / 2,
  }
};

